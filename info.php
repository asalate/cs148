<h2>What is The Wall?</h2>

<p>The Wall is a place for professors or students to see their student or classmates. So far it's only for 
Robert Erickson and students in his classes, but it could easily expand. The bigger, the better. Soon it can be a place 
for students to update their classes, and if added, a small blog about themselves.<br><br>
Please go and fill out a form to get on the wall! But remember only one picture per person, so choose wisely. </p>
<p>Pretty please consider joining the wall. It's a great place to be and keep up to date on your CS peers!</p>

<h1>Why should I be on The Wall of Photos?</h1>

<p>Being very general, there are three kinds of students. Those who like to be involved, those who sometimes like to be involved and 
those who do not want to be involved. Whatever kind of student you are or were, you were a part of the UVM community. So even if you don't 
like to be involved it's always nice to have a picture of everyone. Months, years, or decades down the line you can always look
back at yourself and remember the past. The Wall is more than just a picture of a student, The Wall is a place to remember and keep 
track of students whether new or old.</p>

